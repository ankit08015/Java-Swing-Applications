       public void populateTable(){
        
        DefaultTableModel dtm= (DefaultTableModel) viewCarsJTable.getModel();
        
        dtm.setRowCount(0);
        
        HashSet<String> uniqCar= new HashSet<>();
        
        for(Car c: cc.getCarCatalogue()){
            uniqCar.add(c.getManufacturer());
        }
        
    
        
        for(String c : uniqCar){
            
            Object [] row= new Object[2];
            row[0]=c;
          //row[1]=c.getModelNumber();
            dtm.addRow(row);
        }
        
    }
    
    


