    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow= tblAccounts.getSelectedRow();
        
        if(selectedRow>=0){
            
            int dialogButton= JOptionPane.YES_NO_OPTION;
            int dialogResult= JOptionPane.showConfirmDialog(null, "Would you like to delete the selected account", "Warning", dialogButton );
            if(dialogResult==JOptionPane.YES_OPTION){
                Account acc= (Account) tblAccounts.getValueAt(selectedRow, 0);
                        
                        accDir.deleteAccount(acc);
                        
                        populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void viewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblAccounts.getSelectedRow();
        
        if(selectedRow>=0){
            
          
                Account acc= (Account) tblAccounts.getValueAt(selectedRow, 0);
                ViewAccountDetailsJPanel panel= new ViewAccountDetailsJPanel(userProcessContainer,acc);
                userProcessContainer.add("ViewAccountDetailsJPanel", panel);
                CardLayout layout= (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);               
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row first to view details.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_viewDetailsBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        
        if(searchTxtField.getText().equals("")){
               
       JOptionPane.showMessageDialog(null, "Please enter account number to search.", "Information", JOptionPane.WARNING_MESSAGE);
 
        }
        else{
        
        Account result= accDir.searchAccountNumber(searchTxtField.getText());
        
        if(result==null){
            
       JOptionPane.showMessageDialog(null, "Account number you entered does not exist.", "Information", JOptionPane.WARNING_MESSAGE);

        }
        
        else{
            
              ViewAccountDetailsJPanel panel= new ViewAccountDetailsJPanel(userProcessContainer,result);
                userProcessContainer.add("ViewAccountDetailsJPanel", panel);
                CardLayout layout= (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer); 
            
        }
        searchTxtField.setText("");
        }
    }//GEN-LAST:event_searchBtnActionPerformed


