    public void populateTable(){
        
        DefaultTableModel dtm= (DefaultTableModel) tblAccounts.getModel();
        
        dtm.setRowCount(0);
        
        for(Account acc: accDir.getAccList()){
            Object[] row= new Object [4];
            row[0]= acc;
            row[1]= acc.getRoutingNum();
            row[2]= acc.getAccNum();
            row[3]= acc.getBalance();
            
            dtm.addRow(row);
            
        }
    }

