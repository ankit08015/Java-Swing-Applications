    private void loginJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJBtnActionPerformed
        // TODO add your handling code here:

        String userName = userNameJTxtField.getText().trim();
        String pwd = passwordJTxtField.getText().trim();
        boolean flag=false;

        for (UserAccount ua : business.getUserAccDir().getUserAccList()) {
            if (ua.getUserName().equals(userName) && ua.getPassword().equals(pwd)) {
                flag=true;
                userProcessContainer.removeAll();

                if (ua.getRole().equals(UserAccount.ADMIN_ROLE)) {
                    AdminWorkAreaJPanel admin = new AdminWorkAreaJPanel(userProcessContainer, business);
                    userProcessContainer.add("AdminWorkAreaJPanel", admin);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                    break;

                } else if (ua.getRole().equals(UserAccount.EMPLOYEE_ROLE)) {
                    EmployeeWorkAreaJPanel empWa = new EmployeeWorkAreaJPanel( userProcessContainer, ua);
                    userProcessContainer.add("EmployeeWorkAreaJPanel", empWa);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                    break;
                }
            }
        }
        
        if(flag==false){
            JOptionPane.showMessageDialog(this, "Invalid user name and/or password", "Error", JOptionPane.ERROR_MESSAGE); 
            return;
        }
        
        userNameJTxtField.setEnabled(false);
        passwordJTxtField.setEnabled(false);
        loginJBtn.setEnabled(false);
        logoutJBtn.setEnabled(true);
        
       
    }//GEN-LAST:event_loginJBtnActionPerformed

    private void logoutJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutJBtnActionPerformed
        // TODO add your handling code here:
        userNameJTxtField.setText("");
        passwordJTxtField.setText("");

        userNameJTxtField.setEnabled(true);
        passwordJTxtField.setEnabled(true);
        loginJBtn.setEnabled(true);
        logoutJBtn.setEnabled(false);

        userProcessContainer.removeAll();
        JPanel panel = new JPanel();
        userProcessContainer.add("panel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_logoutJBtnActionPerformed

