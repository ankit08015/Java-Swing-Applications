    public void populateTable() {

        int rowCount = bookFlightJTable.getRowCount();

        DefaultTableModel dtm = (DefaultTableModel) bookFlightJTable.getModel();

        for (int i = rowCount - 1; i >= 0; i--) {
            dtm.removeRow(i);
        }

        try {
            for (Flight f : flights) {
                
                Object[] row = new Object[7];
                row[0] = f;
                row[1] = f.getAirlinerName();
                row[2] = f.getFromLocation();
                row[3] = f.getToLocation();
                
                row[4] = f.getDate();
                row[5] = f.getTime();
                row[6] = f.getPrice();
                
                dtm.addRow(row);
            }
        } catch (Exception e) {
        }
    }
    
