    private void bookJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookJButtonActionPerformed
        // TODO add your handling code here:
       int selectedRow= bookFlightJTable.getSelectedRow();
         
             if(selectedRow<0) {
             JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
      
             }
         Flight f= (Flight) bookFlightJTable.getValueAt(selectedRow, 0);
         
         FinalBookingJPanel panel= new FinalBookingJPanel(userProcessContainer, f, travelAgency, cust);
         userProcessContainer.add("FinalBookingJPanel", panel);
         CardLayout layout= (CardLayout) userProcessContainer.getLayout();
         layout.next(userProcessContainer);


    }//GEN-LAST:event_bookJButtonActionPerformed

