    private void addAirlinerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAirlinerJButtonActionPerformed
        // TODO add your handling code here:
        
        if(addAirlinerTxtField.getText().equals("")){
            
              JOptionPane.showMessageDialog(null, "Airliner name can not be blank", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
            
        }
        
        for(Airliner airliner: travelAgency.getMasterSchedule()){
        
        if(airliner.getName().equalsIgnoreCase(addAirlinerTxtField.getText())){
            JOptionPane.showMessageDialog(null, "Same airliner is already created. Please create new or access through Airliner paanel.", "Warning", JOptionPane.WARNING_MESSAGE);
            addAirlinerTxtField.setText("");
            return;
        }
    }
        
        Airliner airliner= travelAgency.addAirliner();
        
        airliner.setName(addAirlinerTxtField.getText());
        
        JOptionPane.showMessageDialog(null, "Airliner created successfully !!!" );
        addAirlinerTxtField.setText("");
    }//GEN-LAST:event_addAirlinerJButtonActionPerformed

