    private void viewCustJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewCustJButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = custDetailsJTable.getSelectedRow();

        if (selectedRow >= 0) {

            Customer cust = (Customer) custDetailsJTable.getValueAt(selectedRow, 0);

            ViewCustomerJPanel viewCust = new ViewCustomerJPanel(userProcessContainer, cust);

            userProcessContainer.add("ViewCustomerJPanel", viewCust);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to view.");
        }

    }//GEN-LAST:event_viewCustJButtonActionPerformed

    private void deleteCustJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCustJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = custDetailsJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;

        }

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the selected Customer?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            Customer c = (Customer) custDetailsJTable.getValueAt(selectedRow, 0);
            travelAgency.getCustomerDirectory().remove(c);
            JOptionPane.showMessageDialog(null, "Customer deleted successfully.", "Warning", JOptionPane.WARNING_MESSAGE);

            populateTable();
        }


    }//GEN-LAST:event_deleteCustJButtonActionPerformed


