    public void populateTable() {

        int rowCount = custDetailsJTable.getRowCount();

        DefaultTableModel dtm = (DefaultTableModel) custDetailsJTable.getModel();

        // dtm.setRowCount(0);
        for (int i = rowCount - 1; i >= 0; i--) {
            dtm.removeRow(i);
        }

        for (Customer c : travelAgency.getCustomerDirectory()) {

            Object[] row = new Object[1];
            row[0] = c;

            dtm.addRow(row);
        }

    }

