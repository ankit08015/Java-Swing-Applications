    private void viewAirlinerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = airlinerDetailsJTable.getSelectedRow();

        if (selectedRow >= 0) {

            Airliner airliner = (Airliner) airlinerDetailsJTable.getValueAt(selectedRow, 0);

            ViewAirlinerJPanel viewAirliner = new ViewAirlinerJPanel(userProcessContainer, airliner);

            userProcessContainer.add("ViewAirlinerJPanel", viewAirliner);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to view.");
        }


    }//GEN-LAST:event_viewAirlinerJButtonActionPerformed

    private void deleteAirlinerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAirlinerJButtonActionPerformed
        // TODO add your handling code here:
        int row = airlinerDetailsJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the selected Airline?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            Airliner airliner = (Airliner) airlinerDetailsJTable.getValueAt(row, 0);

            travelAgency.removeAirliner(airliner);
            JOptionPane.showMessageDialog(null, "Airliner has been successfully deleted.", "Information", JOptionPane.INFORMATION_MESSAGE);

            populateTable();
        }


    }//GEN-LAST:event_deleteAirlinerJButtonActionPerformed


