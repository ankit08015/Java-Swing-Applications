    private void viewTicketJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewTicketJBtnActionPerformed
        // TODO add your handling code here:

        int selectedRow = customerBookingJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;

        }
        Ticket ticket = (Ticket) customerBookingJTable.getValueAt(selectedRow, 0);

        ViewFlightDetailsJPanel panel = new ViewFlightDetailsJPanel(userProcessContainer, ticket);
        userProcessContainer.add("ViewFlightDetailsJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_viewTicketJBtnActionPerformed

