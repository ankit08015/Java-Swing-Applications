    private void cancelTicketJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelTicketJButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = customerBookingJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;

        }

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to cancel the selected ticket?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            Ticket ticket = (Ticket) customerBookingJTable.getValueAt(selectedRow, 0);

            cust.cancelTicket(ticket);

            JOptionPane.showMessageDialog(null, "Ticket has been successfully cancelled.", "Warning", JOptionPane.WARNING_MESSAGE);

            populateTable();
        }


    }//GEN-LAST:event_cancelTicketJButtonActionPerformed


