    private void AddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSupplierActionPerformed
        // TODO add your handling code here:
        try
        {
        String Name = nameTxtField.getText().trim();
        if(Name.matches(".*\\d.*"))
        {
           JOptionPane.showMessageDialog(null,"Please enter the valid supplier name with no digits");   
           return;
        }
        
        String Location = LocationTxtField.getText().trim();
        if(Location.matches(".*\\d.*"))
        {
           JOptionPane.showMessageDialog(null,"Please enter the valid location name with no digits");
           return;
        }
        
        String Zip = ZipCodeTxtField.getText().trim();
        int zip2 = 0;
        try {
            zip2 = Integer.parseInt(ZipCodeTxtField.getText().trim());
        } catch (NumberFormatException numberFormatException) {
                JOptionPane.showMessageDialog(this,"Please enter numeric value for zip code","Error", JOptionPane.ERROR_MESSAGE);
            return;
            
        }

        if(Name.equals("")|| Location.equals("")||Zip.equals("")|| UserNameTxtField.equals("")||PasswordTxtField.equals("")){
            JOptionPane.showMessageDialog(this,"Please enter all the fields","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Supplier s = business.getSupplierDirectory().addSupplier();
        s.setSupplier_name(Name);
        s.setSupplier_location(Location);
        s.setZip_code(zip2);
        
        UserAccount ua= business.getUserAccountDirectory().addUserAccount();
        
        ua.setUserName(UserNameTxtField.getText());
        ua.setPassword(PasswordTxtField.getText());
        ua.setRole(UserAccount.SUPPLIER_ROLE);
            if(activeRadioBtn.isSelected()){
            ua.setIsActive(true);
        }
        else{
            ua.setIsActive(false);
        }
            s.setUserAccount(ua);
        
        JOptionPane.showMessageDialog(this,"Supplier has been saved","Info", JOptionPane.INFORMATION_MESSAGE);

        nameTxtField.setText("");
        LocationTxtField.setText("");
        ZipCodeTxtField.setText("");
         UserNameTxtField.setText("");
        PasswordTxtField.setText("");

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid values");
        }
    }//GEN-LAST:event_AddSupplierActionPerformed

