    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            Product product;
            int productId = Integer.parseInt(idField.getText());
            product = sup.getProductDirectory().SearchProd(productId);
            if(product!= null) {

                Interface.Supplier.ViewProductsJPanel issvpjp = new Interface.Supplier.ViewProductsJPanel(userProcessContainer, product);
                userProcessContainer.add("Interface.Supplier.ViewProductsJPanel", issvpjp);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);

            }
            else
            {
                JOptionPane.showMessageDialog(null,"Nothing found");
            }
        }

        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Nothing found");
        }

    }//GEN-LAST:event_searchButtonActionPerformed


