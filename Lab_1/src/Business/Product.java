package Business;

/**
 *
 * @author ankit
 */
public class Product {
    
    private String name;
    private String price;
    private String checkAval;
    private String desc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCheckAval() {
        return checkAval;
    }

    public void setCheckAval(String checkAval) {
        this.checkAval = checkAval;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
    
    
    
}
