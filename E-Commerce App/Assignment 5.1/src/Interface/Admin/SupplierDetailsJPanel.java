    public void populateProductTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) prodListJTbl.getModel();
        dtm.setRowCount(0);
        
        for(Product p: s.getProductDirectory().getProductDirectory()){
            Object row[]= new Object[4];
            row[0]= p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            row[3]= p.getAvail();
            dtm.addRow(row);
            
        }
        
    }
    
    public void PopulateOrderTable() {

        DefaultTableModel dtm = (DefaultTableModel) orderListJTbl.getModel();
        dtm.setRowCount(0);

        for (Entry<OrderItem, Customer> e : s.getSuppOrderList().entrySet()) {
            OrderItem oi = e.getKey();
            Customer c = e.getValue();
            Object row[] = new Object[5];
            row[0] = c.getFirstName() + " " + c.getLastName();
            row[1] = oi.getProduct();
            row[2] = oi.getQuantity();
            row[3] = oi.getSalesPrice();
            row[4] = oi.getOrderStatus();
            dtm.addRow(row);

        }

    }

