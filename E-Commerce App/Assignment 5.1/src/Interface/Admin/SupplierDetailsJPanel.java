    private void saveJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJBtnActionPerformed
        // TODO add your handling code here:
        
        String suppName= suppNameTxtField.getText();
        
        if(suppName.equals("")){
            
             JOptionPane.showMessageDialog(this, "Supplier name can not be blank","Error",JOptionPane.ERROR_MESSAGE);
            
        }
        else{
            s.setSupplier_name(suppName);
             JOptionPane.showMessageDialog(this, "Supplier name updated successfully","Success",JOptionPane.INFORMATION_MESSAGE);
             suppNameTxtField.setText(s.getSupplier_name());
                suppNameTxtField.setEnabled(false);
                updateJBtn.setEnabled(true);
                saveJBtn.setEnabled(false);
        }
    }//GEN-LAST:event_saveJBtnActionPerformed

